public class DataTypes {
    public static void main(String[] args) {
        int i = 10;//4bytes -> 32bit -> -2^31 to 2^31 - 1
        long l = 20;//8bytes -> 64bit -> -2^63 to 2^63 - 1
        short s = 30;//2bytes -> 16bit -> -2^15 to 2^15 - 1
        byte b = 12;//1byte -> 8bit ->. -2^7 to 2^7 - 1
        System.out.println(b);
    }
}
